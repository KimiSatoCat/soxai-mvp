export const ALL_SPECIES = [MUSCAT, PEACH, LEMON, GRAPE, ORANGE, BLUEBERRY, CHERRY];

export const SPECIES_COUNT = ALL_SPECIES.length; // 7

/**
 * ID からキャラ系統を取得
 */
export function getSpeciesById(id) {
  return ALL_SPECIES.find((s) => s.id === id) ?? ALL_SPECIES[0];
}

/**
 * インデックスからキャラ系統を取得
 */
export function getSpeciesByIndex(idx) {
  return ALL_SPECIES[idx % SPECIES_COUNT];
}

/**
 * レベルから進化段階インデックス (0-3) を返す
 */
export function getStageIndex(level) {
  if (level < 25) return 0;
  if (level < 50) return 1;
  if (level < 70) return 2;
  return 3;
}

/**
 * レベルに対応する段階データを返す
 */
export function getStageForLevel(species, level) {
  const idx = getStageIndex(level);
  return species.stages[idx];
}
